    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillCategories();
        this.fillDrinks();
    }

    @Override
    public void fillCategories() {
        DefaultTableModel model = (DefaultTableModel) tblCategories.getModel();
        model.setRowCount(0);

        categories = categoryDAO.findAll();
        categories.forEach(category -> {
            model.addRow(new Object[]{category.getName()});
        });
        if (tblCategories.getRowCount() > 0) {
            tblCategories.setRowSelectionInterval(0, 0);
        }
    }

    @Override
    public void fillDrinks() {
        int row = tblCategories.getSelectedRow();
        if (row >= 0) {
            Category category = categories.get(row);

            DefaultTableModel model = (DefaultTableModel) tblDrinks.getModel();
            model.setRowCount(0);

            drinks = drinkDAO.findByCategoryId(category.getId());
            drinks.forEach(drink -> {
                model.addRow(new Object[]{
                    drink.getId(),
                    drink.getName(),
                    String.format("$%.1f", drink.getUnitPrice()),
                    String.format("%.0f%%", drink.getDiscount() * 100)
                });
            });
        }
    }

    @Override
    public void addDrinkToBill() {
        int row = tblDrinks.getSelectedRow();
        if (row >= 0) {
            Drink drink = drinks.get(row);
            String quantity = XDialog.prompt("Số lượng?");
            if (quantity != null && quantity.length() > 0) {
                try {
                    int qty = Integer.parseInt(quantity);
                    if (qty > 0) {
                        this.drink = drink;
                        this.dispose();
                    } else {
                        XDialog.alert("Số lượng phải lớn hơn 0!");
                    }
                } catch (NumberFormatException e) {
                    XDialog.alert("Số lượng không hợp lệ!");
                }
            }
        }
    }

    public Drink getDrink() {
        return drink;
    }
